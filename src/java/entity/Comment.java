package entity;

/**
 *
 * @author ERDEM
 */
public class Comment {
    
    private long id ;
    private String text ;
    private Course course ;
    private User user ;

    public Comment() {
    }

    public Comment(long id, String text, Course course, User user) {
        this.id = id;
        this.text = text;
        this.course = course;
        this.user = user;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public Course getCourse() {
        return course;
    }

    public void setCourse(Course course) {
        this.course = course;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
    
    
}
